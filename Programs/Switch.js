let fruit = "apple";

switch (fruit) {
  case "banana": console.log("It's a banana!");
    break;
  case "apple":  console.log("It's an apple!");
    break;
  case "orange":  console.log("It's an orange!");
    break;
  default:
    console.log("Unknown fruit.");
}

